public class CharDemo {


    public static void main(String[] args) {
        // 字符赋值要使用单引号
        // 字符只能单个单个赋值
        char nameFirstChar = 'k';
        // 本质上是整数，属于事物的一体两面。
        char nameSecondChar = 97;
        //
        System.out.println(nameFirstChar);
        System.out.println(nameSecondChar);

    }
}
